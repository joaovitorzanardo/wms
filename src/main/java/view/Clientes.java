    private void btnSearchCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchCustomerActionPerformed
        String paramType = cbxParam.getSelectedItem().toString();
        List<Customer> customers = new ArrayList<>();

        switch (paramType) {
            case "Todos":
                customers = customerDAO.getAllCustomers();
                break;
            case "Nome":
                customers = customerDAO.getCustomersByName(txtInputParam.getText().trim());
                break;
            case "Email":
                Customer customerEmail = customerDAO.getCustomerByEmail(txtInputParam.getText().trim());
                customers.add(customerEmail);
                break;
            case "CPF":
                Customer customerCpf = customerDAO.getCustomerByCPF(txtInputParam.getText().trim());
                customers.add(customerCpf);
                break;
        }

        DefaultTableModel tblModel = (DefaultTableModel) tblClientes.getModel();
        tblModel.setRowCount(0);
        for (Customer cust : customers) {
            String data[] = {cust.getCustomerId().toString(), cust.getFirstName(), cust.getLastName(), cust.getEmail(), cust.getCpf(), cust.getPhoneNumber(), cust.getFullAddress()};
            tblModel.addRow(data);
        }
    }//GEN-LAST:event_btnSearchCustomerActionPerformed

