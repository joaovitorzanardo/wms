    private void fillAddressComboBox() {
        List<Address> addresses = addressDAO.getAllAdresses();
        ArrayList<Map.Entry<Integer, String>> a = new ArrayList<Map.Entry<Integer, String>>();
        for (Address ad : addresses) {
            String enderecoCompleto = ad.getStreet() + ", " + ad.getNumber().toString() + " - " + ad.getCity() + ", " + ad.getUf();
            cbxEnderecos.addItem(enderecoCompleto);
        }  
    }
